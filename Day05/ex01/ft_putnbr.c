void	ft_putnbr(int nb)
{
	int i;
	int tmp;
	int x;

	x = 0;
	i = 1;
	tmp = 1;
	while ((nb / i) > 0)
	{
		i = i * 10;
	}
	while (nb > 0)
	{
		x = nb / i;
		nb = nb % i;
		i = i / 10;
		ft_putchar(x);
	}
}
